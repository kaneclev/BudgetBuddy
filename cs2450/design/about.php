	<h3>Purpose & Why</h3>
		<p>I decided to do this project because I always find myself manually keeping track of finances in all sorts of notes and docs strewn across my PC, so this seemed cool.</p>	
		<p>I did NOT think it would end up being as much work as it was; it ended up taking an extreme amount of time for it all to come together.</p>
	<br>
	<h3>Utility</h3>
		<p>Use the budget page to create categories for expenses. A category might be utilities, or entertainment, or whatever else. </p>
		<p>You can also use the budget page to create categories for income streams in a similar manner.</p>
		<p>Once categories are created, use the 'Expense' and 'Income' page to create records of expenses which fall into each of the categories you have created</p>
		<p>As you record your expenses in these various categories, the dashboard will populate with statistics and charts regarding your financial information.</p>
		<p>By creating an account, your financial information is stored and you may return to it in the same state whenever you please.</p>
